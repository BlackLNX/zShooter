void draw_img(const uchar img[], int w, int h) {
    uchar c;
    int i;
    for (int y=0; y<h; y++) {
        for (int x=0; x<w; x++) {
            i = y * w / 2 + (x >> 1);
            c = img[i];
            if (x % 2) c = c & 15;
            else c = c >> 4;
            if (c > 0) printf("%s██", F_COLOR[c]);
            else printf("  ");
        }
        printf("%s\n", RESET);
    }
    fflush(stdout);
}

void draw_img_small(const uchar img[], int w, int h) {
    uchar c1, c2;
    int i;
    for (int y=0; y<h; y+=2) {
        for (int x=0; x<w; x++) {
            i = y * w / 2 + (x >> 1);
            c1 = img[i];
            c2 = img[i + 8];
            if (x % 2) {
                c1 = c1 & 15;
                c2 = c2 & 15;
            } else {
                c1 = c1 >> 4;
                c2 = c2 >> 4;
            }
            printf("%s", RESET);
            printf("%s", F_COLOR[c2]);
            printf("%s", B_COLOR[c1]);
            fflush(stdout);
            printf("%s", HALF_CHAR);
        }
        printf("%s\n", RESET);
    }
    fflush(stdout);
} 
